// ─── Filename classifier ──────────────────────────────────────────────────────
export function categorizeByFilename(filename: string): Category | null {
  const name = filename.toLowerCase();
  for (const rule of FILENAME_RULES) {
    for (const pattern of rule.patterns) {
      if (pattern.test(name)) {
        if (rule.exclude && rule.exclude.some(ex => ex.test(name))) break;
        return rule.category;
      }
    }
  }
  return null;
}

// ─── OCR priority order (first match wins — do NOT change order) ──────────────
const OCR_PRIORITY: Category[] = [
  "OTP / Security",
  "Payments / Receipts",
  "WhatsApp / Chats",
  "Social Media",
  "Documents",
  "Study / Notes",
  "Memes / Entertainment",
  "Photos",
];

// ─── OCR text classifier ──────────────────────────────────────────────────────
export function categorizeByText(rawText: string): Category | null {
  const text = rawText.toLowerCase();

  // Long text (> 600 chars) means it's likely a conversation / app UI screenshot
  // that only mentions payment words in passing. Real receipts are concise, but
  // even long bank statements have many payment keywords (bank name + transaction
  // type + amount = score 8+). Raise the bar for Payments/Receipts to avoid
  // false positives while keeping all genuine receipts classified correctly.
  const isLongText = text.length > 600;

  // Collect every category whose minScore is met
  const matched = new Set<Category>();
  for (const rule of OCR_RULES) {
    let score = 0;
    for (const kw of rule.keywords) {
      const hit = kw.pattern ? kw.pattern.test(text) : text.includes(kw.text);
      if (hit) score += kw.weight;
    }
    const effectiveMinScore =
      rule.category === "Payments / Receipts" && isLongText ? 6 : rule.minScore;
    if (score >= effectiveMinScore) matched.add(rule.category);
  }

  if (!matched.size) return null;

  // Return the highest-priority match, not the highest-scoring one
  for (const cat of OCR_PRIORITY) {
    if (matched.has(cat)) return cat;
  }

  // Fallback: any match not in priority list (shouldn't happen)
  return [...matched][0];
}

/** Combined pipeline: filename → OCR text → Unknown */
export function classify(filename: string, ocrText?: string): Category {
  const byName = categorizeByFilename(filename);
  if (byName) return byName;

  if (ocrText?.trim()) {
    const byText = categorizeByText(ocrText);
    if (byText) return byText;
  }

  return "Unknown / Others";
}
